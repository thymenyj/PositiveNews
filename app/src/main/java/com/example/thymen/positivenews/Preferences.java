package com.example.thymen.positivenews;

public class Preferences {
    float business;
    float entertainment;
    float health;
    float science;
    float sports;
    float technology;


    public float getBusiness() {
        return business;
    }

    public void setBusiness(float business) {
        this.business = business;
    }

    public float getEntertainment() {
        return entertainment;
    }

    public void setEntertainment(float entertainment) {
        this.entertainment = entertainment;
    }

    public float getHealth() {
        return health;
    }

    public void setHealth(float health) {
        this.health = health;
    }

    public float getScience() {
        return science;
    }

    public void setScience(float science) {
        this.science = science;
    }

    public float getSports() {
        return sports;
    }

    public void setSports(float sports) {
        this.sports = sports;
    }

    public float getTechnology() {
        return technology;
    }

    public void setTechnology(float technology) {
        this.technology = technology;
    }

}
